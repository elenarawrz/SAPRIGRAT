	void addMenu(String titulo, Formulario form)
	{
		addItems(mbrMenu.addItem(titulo, null), form);
	}
	
	void addSubMenu(MenuItem menuPadre, String titulo, Formulario form)
	{
		addItems(menuPadre.addItem(titulo, null), form);
	}
	
	void addItems(MenuItem menu, Formulario form)
	{
		menu.addItem("Agregar nuevo", com(form, 1));
		menu.addItem("Modificar existente", com(form, 2));
		menu.addSeparator();
		menu.addItem("Listado", com(new Listado(form.getRestricciones()), form.getEntidad()));
	}
	
	Command com(final Formulario form, final int tipo)
	{
		return  new Command()
				{
					@Override
					public void menuSelected(MenuItem selectedItem)
					{
						String path = "";
						for (MenuItem parent = selectedItem.getParent(); parent!=null; parent = parent.getParent())
							path = parent.getText() + "  >  " + path;
						lblOpcionSel.setValue(path + selectedItem.getText());
						absLOpcionSel.setVisible(true);
						lblOpcionSel.setVisible(true);
						form.inicializar();
						form.setTipo(tipo);
						horLBody.removeAllComponents();
						horLBody.addComponent(form);
						horLBody.setComponentAlignment(form, new Alignment(20));
						
					}
				};
	}

