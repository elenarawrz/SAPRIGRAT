	private ClickListener agregarListener = new ClickListener()
											{
												@Override
												public void buttonClick(ClickEvent event)
												{
													if(validar())
													{
														LocalDate fecha = datIngreso.getLocalDate();
														String curr =   setFormato("00", usuario.PRODUCTOR) +
																		setFormato("00", fecha.getYearOfCentury()) +
																		setFormato("00", fecha.getMonthOfYear()) +
																		setFormato("00", fecha.getDayOfMonth()) +
																		setFormato("00", estados.indexOf(cmbEdo.getValue()) + 1);

														LinkedList<Object> valores = llenarListaDeParametros();
														valores.add(0, curr);
														valores.add(5, getFecha(datIngreso));
														
														curr = datos.registrarProductor(valores);
														if(!curr.isEmpty())
														{
															String[]currArr = curr.split(" ");
															Notification.show("Registro añadido correctamente.\nCURR asignado: " + currArr[0], Notification.Type.TRAY_NOTIFICATION);
															txtCURR.setValue(currArr[0]);
															//Se cambia a modo de modificación
															setTipo(MODO_MODIFICAR);
															idProductor = Integer.parseInt(currArr[1]);
														}
														else
															Notification.show("Error al añadir registro, comuníquese con el administrador.", Notification.Type.ERROR_MESSAGE);
													}
												}
												
												private String setFormato(String formato, int numero)
												{
													return new DecimalFormat(formato).format(numero);
												}
												
												private Date getFecha(TuningDateField calendario)
												{
													if(calendario.getValue() == null)
														return null;
													else
														return new Date(calendario.getLocalDate().toDate().getTime());
												}
											};
