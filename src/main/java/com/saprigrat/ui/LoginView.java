	ClickListener submitLogin = new ClickListener()
		{
			@Override
			public void buttonClick(ClickEvent event)
			{
				if(checkPass(txtUser.getValue(), txtPass.getValue()))
				{
					guardarSesion();
					mainLayout.removeAllComponents();
					mainLayout.addComponent(new MainView(ui));
					Notification.show("Bienvenido " + usuario.getNombre(), Notification.Type.TRAY_NOTIFICATION);
				}
				else
					Notification.show("El nombre de usuario no coincide con la contrase침a.", Notification.Type.WARNING_MESSAGE);
			}

	        // Guardar a VaadinServiceSession
			private void guardarSesion()
			{
				ui.getSession().setAttribute("user", txtUser.getValue());
				ui.getSession().setAttribute("pass", txtPass.getValue());
				ui.getSession().setAttribute("usuario", usuario);
			}
		};
